			<p class="intro">Dieter Rams (born 20 May 1932 in Wiesbaden, Hessen) is a German industrial designer closely associated with the consumer products company Braun and the Functionalist school of industrial design.</p>
			<p>Rams began studies in architecture and interior decoration at Wiesbaden School of Art in 1947. Soon after in 1948, he took a break from studying to gain practical experience and conclude his carpentry apprenticeship. He resumed studies at Wiesbaden School of Art in 1948 and graduated with honours in 1953 after which he began working for Frankfurt based architect Otto Apel. In 1955, he was recruited to Braun as an architect and an interior designer. In addition, in 1961, he became the Chief Design Officer at Braun until 1995.</p>
			<p><span id="more-1892"></span></p>
			<div id="attachment_21" style="width: 310px" class="wp-caption alignright"><img src="http://andersnoren.se/themes/hemingway/wp-content/uploads/2014/01/tumblr_mnh0n9pHJW1st5lhmo1_1280-766x510-300x199.jpg" alt="Text" width="300" height="199" class="size-medium wp-image-21" /><p class="wp-caption-text">Image description</p></div>
				<p>Dieter Rams was strongly influenced by the presence of his grandfather, a carpenter. Rams once explained his design approach in the phrase “Weniger, aber besser” which translates as “Less, but better”. Rams and his staff designed many memorable products for Braun including the famous SK-4 record player and the high-quality ‘D’-series (D45, D46) of 35 mm film slide projectors. He is also known for designing the 606 Universal Shelving System by Vitsœ in 1960.</p>
			<blockquote>
				<p>“What Dieter Rams and his team at Braun did was to produce hundreds of wonderfully conceived and designed objects: products that were beautifully made in high volumes and that were broadly accessible.”</p>
				<p><cite>Jony Ive, Apple’s Senior Vice President of Industrial Design</cite></p>
			</blockquote>
			<p>By producing electronic  gadgets that were remarkable in their austere aesthetic and user friendliness, Rams made Braun a household name in the 1950s. He is considered to be one of the most influential industrial designers of the 20th century.</p>
			
